package ex_03_DataTypes;

public class LAB008_DefaultValue_DataTypes {
	
	/*
	 * To get the values of default in data type 
	 * you have to add static before the data type 
	 * add the data type in class body 
	 * and the output statement in main method body 
	*/
	
	static boolean a;
	static byte b;
	static int c;
	static long d; 
	static short h;
	static float e;
	static double f;
	static char g;
		
	public static void main(String[] args) {
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
	}
}